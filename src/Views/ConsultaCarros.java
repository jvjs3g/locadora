    public void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        ConnnectionFactory connection = new ConnnectionFactory();
        ConsulCarro consulta = new ConsulCarro(connection);
        modelo.setNumRows(0);
        for(carro c:consulta.read()){
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCor(),
                c.getPlaca(),
                c.getMarca(),
                c.getPreco(),
                c.getQuantidade(),
                c.getCompleto()
            });
        }
    }
    
    public void filtro(String marca){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        ConnnectionFactory connection = new ConnnectionFactory();
        ConsultaCarroFiltro filtro = new ConsultaCarroFiltro(connection);
        modelo.setNumRows(0);
        for(carro c:filtro.filtro(marca)){
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCor(),
                c.getPlaca(),
                c.getMarca(),
                c.getPreco(),
                c.getQuantidade(),
                c.getCompleto()
            });
        }
    }

