    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        ConnnectionFactory connection = new ConnnectionFactory();
        createCarro c = new createCarro(connection);
        carro car = new carro();
        
        car.setCor(txtCor.getText());
        car.setMarca(txtMarca.getText());
        car.setPlaca(txtPlaca.getText());
        car.setPreco(Float.parseFloat(txtPreco.getText()));
        car.setQuantidade(Integer.parseInt(txtQtd.getText()));
        car.setCompleto(txtCompleto.getText());
        c.create(car);
        JOptionPane.showMessageDialog(null,"Dados salvos com sucesso !!");
    }//GEN-LAST:event_btnSalvarActionPerformed

